    <h2>What is Tết?</h2><br/>
    <p>Tết Nguyên Đán, commonly known as Tết, is the most important holiday in Vietnam. It is the Vietnamese New Year which is based on the lunisolar calendar.</p>
    <p>Tết is celebrated from the first day of the first month of the lunisolar calendar (around late January or early February) until at least the third day. Many Vietnamese prepare for Tết by cooking special holiday foods and cleaning the house. On Tết, Vietnamese visit their families and temples, forget about the troubles of the past year and hope for a better upcoming year. Tết traditionally marks the coming of Spring, so Spring is sometimes used interchangeably with Tết in Vietnamese.</p>
    <img class="ms-vn-logo ml-2 mb-2" style="float: right; max-width: 50%;" src="../../assets/tet-festival-2.jpg">
    <h2>Customs</h2><br/>
    <p>Vietnamese people usually return to their families during Tết. Some return to worship at the family altar or visit the graves of their ancestors. Others return to where they grew up. Although Tết is a universal holiday among all Vietnamese, each region and religion has its own customs.</p>
    <p>Generally, Tết in the three Vietnamese regions can be divided into three periods, known as Tất Niên, Giao Thừa, and Tân Niên, representing the preparation before Tết, the eve of Tết, and the days of and following Tết, respectively.</p>
